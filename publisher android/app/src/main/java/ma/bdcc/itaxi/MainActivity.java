        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Constants.context=this.getApplicationContext();

        EditText editText=(EditText)findViewById(R.id.editTextNumberDecimal);
        editText.setText("0");
        EditText destination=(EditText)findViewById(R.id.editTextDestionation);
        destination.setText("");

        findViewById(R.id.buttonStart).setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                Constants.userId=Long.valueOf(editText.getText().toString());
                Constants.userDestination=destination.getText().toString();
                if (ContextCompat.checkSelfPermission(
                        getApplicationContext(), Manifest.permission.ACCESS_FINE_LOCATION
                ) != PackageManager.PERMISSION_GRANTED
                ) {
                    ActivityCompat.requestPermissions(
                            MainActivity.this,
                            new String[]{Manifest.permission.ACCESS_FINE_LOCATION},
                            REQUEST_CODE_LOCATION_PERMISSION
                    );
                } else {
                    startLocationService();
                }
            }
        });

        findViewById(R.id.buttonStop).setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                stopLocationService();
            }
        });

